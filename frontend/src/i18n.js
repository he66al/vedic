import React, { createContext, useContext, useState, useEffect } from "react";

const I18nContext = createContext({ lang: "en", t: (k) => k, setLang: () => {} });

export function I18nProvider({ children }) {
    const [lang, setLang] = useState(() => localStorage.getItem("jk_lang") || "en");
    useEffect(() => { localStorage.setItem("jk_lang", lang); }, [lang]);
    const t = (key) => translations[lang]?.[key] ?? translations.en[key] ?? key;
    return (
        <I18nContext.Provider value={{ lang, setLang, t }}>
            {children}
        </I18nContext.Provider>
    );
}

export function useI18n() {
    return useContext(I18nContext);
}

export function LanguageSwitcher({ testId = "lang-switcher" }) {
    const { lang, setLang } = useI18n();
    const options = [
        { id: "en", label: "EN" },
        { id: "hi", label: "हिं" },
    ];
    return (
        <div
            data-testid={testId}
            className="inline-flex items-center border border-[#C5A059] rounded-sm overflow-hidden bg-[#FCFAF5]"
        >
            {options.map((o) => (
                <button
                    key={o.id}
                    data-testid={`${testId}-${o.id}`}
                    onClick={() => setLang(o.id)}
                    className={`px-3 py-1.5 text-xs font-semibold tracking-wider transition-colors ${
                        lang === o.id
                            ? "bg-[#8B1E0F] text-[#FCFAF5]"
                            : "text-[#2C241B] hover:bg-[#F4F1E8]"
                    }`}
                >
                    {o.label}
                </button>
            ))}
        </div>
    );
}
